import * as React from "react";

import { cn } from "@/lib/utils";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { variant?: "default" | "elevated" | "outline" }
>(({ className, variant = "default", ...props }, ref) => {
  const variantStyles = {
    default: "bg-white dark:bg-gray-800 border-gray-200 dark:border-gray-700",
    elevated: "bg-white dark:bg-gray-800 shadow-xl border-gray-200 dark:border-gray-700 hover:shadow-2xl",
    outline: "bg-transparent border-2 border-gray-300 dark:border-gray-600",
  };

  return (
    <div
      ref={ref}
      className={cn(
        "rounded-2xl border p-4 transition-all duration-300",
        variantStyles[variant],
        className
      )}
      {...props}
    />
  );
});
Card.displayName = "Card";

